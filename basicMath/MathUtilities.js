class MathUtilities {


    add(baseValue, valueToAdd){
        return baseValue + valueToAdd;
    }

    subtract(baseValue, valueToAdd){
        return baseValue - valueToAdd;
    }

    divide(baseValue, valueToAdd){
        return baseValue / valueToAdd;
    }

    multiply(baseValue, valueToAdd){
        return baseValue * valueToAdd;
    }
}

module.exports = MathUtilities;